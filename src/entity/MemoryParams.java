package entity;

public class MemoryParams {

	private int initialMemory;
	private int finalMemory;

	public int getInitialMemory() {
		return initialMemory;
	}

	public int getFinalMemory() {
		return finalMemory;
	}

	public void setInitialMemory(int initialMemory) {
		this.initialMemory = initialMemory;
	}

	public void setFinalMemory(int finalMemory) {
		this.finalMemory = finalMemory;
	}

}
